from typing import List
from copy import copy


class Solution:
    def findItinerary(self, tickets: List[List[str]]) -> List[str]:
        result = []
        path = ['JFK']
        length = len(tickets)

        def backtracking(place_set, ticket_list):
            if len(result) > 0:
                return
            if len(path) == length+1:
                result.append(copy(path))
                return
            for i in place_set:
                if len(result) > 0:
                    return
                path.append(i)
                place_set = sorted(set([j[1] for j in ticket_list if j[0] == i]))
                ticket_list.remove([path[-2], i])
                backtracking(place_set, ticket_list)
                last_place = path.pop()
                ticket_list.append([path[-1], last_place])
        first_place_set = {i[1] for i in tickets if i[0] == 'JFK'}
        backtracking(sorted(first_place_set), tickets)
        return result[0]


